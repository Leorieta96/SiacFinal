        for (Material m : listamateriales) {
            datos[i][0] = m.getNombre();
            datos[i][1] = m.getdescripcion();
            datos[i][2] = m.getStockMaterial();
            datos[i][3] = m.getPrecioUnitario();
            datos[i][4] = m.getRubro();
            i++;
        }

